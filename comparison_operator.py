#comparison operator are used to compare values
#comparison operators are boolean expression which return a boolean value
# a single equal sign is only used to assign a value to a variable
# two or double equal signs are used to determine where the variable values are equal to each other

x = 3 > 2 #greater than (3 > 2) is a boolean expression thus it will return true
m = 5 >= 3#greater than or equal to
n = 3 < 2# less than
e = 1 <= 2# less than or equal to
d = 3 == 4# equality operator: check if the 2 variable values are equal to each other
f = 3 != 9#not equality operator: determines whether the values under study are not equal


print(x)
print(m)
print(n)
print(e)
print(d)
print(f)